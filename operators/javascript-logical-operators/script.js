// AND
console.log(2 > 1 && 5 < 6); // true

console.log(2 > 1 && 7 < 6); // false

// OR
console.log(2 > 1 || 5 < 6); // true

console.log(2 > 1 || 7 < 6); // true

console.log(2 > 3 || 7 < 6); // false

// NOT

console.log(5 > 10); // false

console.log(!(5 > 10)); // true
